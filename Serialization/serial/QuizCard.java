public class QuizCard {
	public QuizCard(String question, String answer) {
		//super();
		this.question = question;
		this.answer = answer;
	}
	String question;
	String answer;
	public String getQuestion() {
		return question;
	}
	public String getAnswer() {
		return answer;
	}
	
	

}
